package de.w33ky;

public class MyColor {
	public float r, g, b, a;
	public MyColor(float r, float g, float b) {
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = 0;
	}
	public MyColor(float r, float g, float b, float a) {
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = a;
	}
}
